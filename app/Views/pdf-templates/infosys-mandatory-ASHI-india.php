        <p>
        The Sexual Harassment of Women at Workplace (Prevention, Prohibition and Redressal) Act, 2013 (hereinafter referred to as the ‘Act’) is the law that provides protection to women against sexual harassment at workplace in India and also details the mechanism in place to deal with complaints of sexual harassment.
        </p>
        <p style="margin-top: 20px;">
        Sexual Harassment is defined under the Act to mean the following unwelcome acts or behavior, amongst others:
        <ol type="a">
            <li>Physical contact and advances; or</li>
            <li>A demand or request for sexual favours; or</li>
            <li>Making sexually coloured remarks; or</li>
            <li>Showing pornography; or</li>
            <li>Any other unwelcome physical, verbal or non-verbal conduct of sexual nature.</li>
        </ol>
        </p>

       <p style="margin-top: 20px;">The Act provides for the constitution of an Internal Committee by every organization, to which an aggrieved woman can complain to, if she is subjected to sexual harassment. </p>

       <p style="margin-top: 20px;">During the pendency of the inquiry before the Internal Committee, the employer can grant the following reliefs:
        <ol type="a">
           <li>
               Transfer the complainant or the respondent to any other workplace; or
           </li>
           <li>
               Grant leave to the complainant for a period up to 3 months; or
           </li>
           <li>
               Recommend to restrain the respondent from reporting on the work performance of the aggrieved woman or writing her confidential report and assign the same to another officer.
           </li>
       </ol>
       </p>
       
       <p style="margin-top: 20px;">
           Under the Act, an inquiry has to be completed by the Internal Committee within 90 (ninety) days.
           On completion of the inquiry, if the Internal Committee comes to the conclusion that the allegation of sexual harassment stands proved, then the Internal Committee can direct the employer to take any one of the following actions:
            <ol type="a">
               <li>
                   Written Apology
               </li>
               <li>
                   Warning to the Respondent
               </li>
               <li>
                   Reprimand or Censure

               </li>
               <li>
                   Withholding of promotion or pay rise or increments
               </li>
               <li>
                   Terminating the Respondent from service
               </li>
               <li>
                   Undergoing a counselling session
               </li>
               <li>
                   Carrying out community service.
               </li>
           </ol>   
        </p>
           

